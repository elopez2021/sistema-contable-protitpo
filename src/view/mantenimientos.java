    private void btnGuardarCatalogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCatalogoActionPerformed
        // TODO add your handling code here:
        JTextField[] campos = {txtNroCuenta, txtDescripcionCuenta, txtCuentaPadre, txtNivelCuenta};
        JLabel[] errorLabels = {errNroCuenta, errDescripcionCuenta, errCuentaPadre, errNivelCuenta};
        boolean camposValidos = true;

        if (cmbGrupoCuenta.getSelectedItem() == "---Seleccione una opción---") {
            errGrupoCuenta.setText("Seleccione una opción");
            camposValidos = false;
        }

        if (!rdbGeneral.isSelected() && !rdbDetalle.isSelected()) {
            errTipoCuenta.setText("Debe seleccionar un tipo de cuenta");
            camposValidos = false;
        } else {
            errTipoCuenta.setText("");
        }

        for (int i = 0; i < campos.length; i++) {
            if (campos[i] == txtCuentaPadre) {
                continue;
            }
            if (!isEmpty(campos[i], errorLabels[i], "Este campo es requerido")) {
                camposValidos = false;
            }
        }

        if (!txtNroCuenta.getText().isEmpty()) {
            int codigo = 0;
            try {
                codigo = Integer.parseInt(txtNroCuenta.getText());
            } catch (NumberFormatException e) {
                errNroCuenta.setText("Debe ser un valor numérico válido");
                camposValidos = false;
            }
        }

        if (!txtNivelCuenta.getText().isEmpty()) {
            int nivel = 0;
            try {
                nivel = Integer.parseInt(txtNivelCuenta.getText());
            } catch (NumberFormatException e) {
                errNivelCuenta.setText("Debe ser un valor numérico válido");
                camposValidos = false;
            }
        }
     if (!catalogoCtrl.existeCuenta(txtCuentaPadre.getText()) && !txtCuentaPadre.getText().isEmpty()) {
            errCuentaPadre.setText("Esta cuenta no existe");
            camposValidos = false;
        }

        if (!camposValidos) {
            return;
        }
        boolean tipo_cta = false;
        if (rdbGeneral.isSelected()) {
            tipo_cta = true;
        }
        int grupo_cta = cmbGrupoCuenta.getSelectedItem().equals("Débito") ? 1 : 2;

        CatalogoCuenta cuenta = new CatalogoCuenta(Integer.parseInt(txtNroCuenta.getText()), txtDescripcionCuenta.getText(), tipo_cta,
                Integer.parseInt(txtNivelCuenta.getText()), !txtCuentaPadre.getText().isEmpty() ? Integer.parseInt(txtCuentaPadre.getText()) : null, grupo_cta,
                LocalDate.now(), LocalTime.now(), 0.00, 0.00, 0.00);
        boolean modificar = catalogoCtrl.existeCuenta(txtNroCuenta.getText());

        if (modificar) {
            if (catalogoCtrl.update(cuenta)) {
                JOptionPane.showMessageDialog(null, "Los datos fueron modificados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                //Limpiar todos los campos
                cmbGrupoCuenta.setSelectedItem("---Seleccione una opción---");
                btnCuentaGroup.clearSelection();
                limpiarCampos(campos);
                lblMensajeCatalogo.setText("");
                tb_load_catalogo();
            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar la cuenta", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            if (catalogoCtrl.save(cuenta)) {
                JOptionPane.showMessageDialog(null, "Los datos fueron guardados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                tb_load_catalogo();
                lblMensajeCatalogo.setText("");
                errGrupoCuenta.setText("");
                limpiarCampos(campos);
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar la cuenta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnGuardarCatalogoActionPerformed


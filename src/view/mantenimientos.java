    public static boolean isEmpty(JTextField textField, JLabel errorLabel, String mensajeError) {
        String texto = textField.getText().trim();
        if (texto.isEmpty()) {
            errorLabel.setText(mensajeError);
            return false;
        } else {
            errorLabel.setText("");
            return true;
        }
    }



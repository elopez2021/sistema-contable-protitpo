    private void txtNroCuentaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNroCuentaFocusLost
        // TODO add your handling code here:
        String nro_cuenta = txtNroCuenta.getText();
        if (catalogoCtrl.existeCuenta(nro_cuenta)) {

            CatalogoCuenta cuenta = catalogoCtrl.buscarCuenta(nro_cuenta);
            if (cuenta != null) {
                if (cuenta.getBalance_cta() != 0.00) {
                    JOptionPane.showMessageDialog(null, "No se puede modificar esta cuenta porque ya cuenta con balance", "Error", JOptionPane.ERROR_MESSAGE);
                    txtNroCuenta.setText("");
                    txtNroCuenta.setEditable(true);
                    return;
                }
                lblMensajeCatalogo.setText("Modificando");
                txtNroCuenta.setText(String.valueOf(cuenta.getNro_cta()));
                txtDescripcionCuenta.setText(cuenta.getDescripcion_cta());
                if (cuenta.isTipo_cta()) {
                    rdbGeneral.setSelected(true);
                } else {
                    rdbDetalle.setSelected(true);
                }
                txtNivelCuenta.setText(String.valueOf(cuenta.getNivel_cta()));
                txtCuentaPadre.setText(cuenta.getCta_padre() == null ? "" : String.valueOf(cuenta.getCta_padre()));

                if (cuenta.getGrupo_cta() == 1) {
                    cmbGrupoCuenta.setSelectedItem("Débito");
                } else if (cuenta.getGrupo_cta() == 2) {
                    cmbGrupoCuenta.setSelectedItem("Crédito");
                }

            }
        } else {
            lblMensajeCatalogo.setText("Creando...");
            txtNroCuenta.setEditable(true);
        }
    }//GEN-LAST:event_txtNroCuentaFocusLost


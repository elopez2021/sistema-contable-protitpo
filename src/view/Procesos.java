    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        Date fechaInicial = fecha_inicial.getDate();
        Date fechaFinal = fecha_final.getDate();

        if (fechaInicial != null && fechaFinal != null) {
            if (fechaFinal.compareTo(fechaInicial) <= 0) {
                JOptionPane.showMessageDialog(this, "La fecha final debe ser mayor o igual que la fecha inicial.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona ambas fechas primero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LocalDate fechaini = fechaInicial.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate fechafin = fechaFinal.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        
        if (procesoCierreCtrl.realizarCierreDiario(fechaini, fechafin)) {
            JOptionPane.showMessageDialog(this, "Proceso de cierre diario completado", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
        }



    }//GEN-LAST:event_btnConsultarActionPerformed

